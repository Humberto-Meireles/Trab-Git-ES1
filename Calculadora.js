// Função que soma dois números
function sum(a, b){
    return a + b;
}

// Função que subtrai dois números
function sub(a, b){
    return a - b;
}

// Função que multiplica dois números
function mult(a, b){
    return a * b;
}

// Função que divide dois números
function div(a, b){
    return a / b;
}
